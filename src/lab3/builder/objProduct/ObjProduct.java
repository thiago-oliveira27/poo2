package lab3.builder.objProduct;

public class ObjProduct{
	
	private String atb1;
	private String atb2;
	private String atb3;
		
	public ObjProduct(String atb1, String atb2, String atb3) {		 
		this.atb1 = atb1;
		this.atb2 = atb2;
		this.atb3 = atb3;
	}

	public String getAtb1() {
		return atb1;
	}

	public String getAtb2() {
		return atb2;
	}

	public String getAtb3() {
		return atb3;
	}
	
	 
}

